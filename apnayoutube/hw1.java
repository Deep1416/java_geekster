package apnayoutube;
import java.util.*;
public class hw1 {
    public static void main(String[] args) {
        int n =5;
        for(int i =1 ; i>=n ;i++){
            for(int j = 1 ; j<=n ;j++){
                System.out.println(" ");
            }
            for(int k = 1; k<n;k++){
                System.out.println("*");
            }
            System.out.println();
        }
    }
}
