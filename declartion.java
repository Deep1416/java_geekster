import java.util.*;
public class declartion {
    public static void main(String[] args) {
        int myvar = 2;
        int x = 34;
        System.out.println(x--);
        System.out.println(++myvar);
        System.out.println(myvar--);
        System.out.println(++myvar);
        System.out.println(--x);
        System.out.println(++x);
        System.out.println(--x);
        System.out.println(myvar + x);
        System.out.println(x = myvar);

    }
    
}
